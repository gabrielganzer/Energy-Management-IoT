fileID = fopen('history2/custom2.txt');
C = textscan(fileID, '%d; %d; %f; %f; %f; %f');
fclose(fileID);
Trun = C{3};
Tidle = C{4};
Tsleep = C{5};
MUrun = mean(Trun);
MUidle = mean(Tidle);
MUsleep = mean(Tsleep);
dlmwrite('transitions.txt', [MUrun MUidle MUsleep], 'delimiter', ' ', 'precision', 6, '-append');